let x;

//Arithmetic operators
x = 5 + 5;
x = 5 - 5;
x = 95 * 95;
x = 5 / 5;
x = 7 % 5;

//Concatenation
x = "Hello" + " " + "World";

//Exponent
x = 2 ** 3;

//Increment
x = 1;
// x = x + 1;
x++;

// Decrement
x--;

// Assignment Operators

x = 10;

x += 5; // x = x + 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;

// Comparison Operators
// Equal to (comparing the value, not the type)
x = 2 == "2";

//equal to (Type and value)
x = 2 === "2";

// equal to (just the value, not the type)
x = 2 != "2";

//equal to (Type and value)
x = 2 !== "2";

//Greater than and less than
x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

console.log(x);
